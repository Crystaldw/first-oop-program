package hwAlgorithms;

/*
Задача
Имея два отсортированных массива размера m и n соответственно, вам нужно найти элемент, который будет находиться на k-й позиции в конечном отсортированном массиве.
Массив 1 - 100 112 256 349 770
Массив 2 - 72 86 113 119 265 445 892
к = 7
Вывод : 256
Окончательный отсортированный массив -
72, 86, 100, 112, 113, 119, 256, 265, 349, 445, 770, 892
7-й элемент этого массива равен 256.

===================================================



Для решения этой задачи можно использовать следующий подход:

Инициализировать переменные i и j для обхода массивов arr1 и arr2.
Пока не достигнута k-я позиция в конечном отсортированном массиве или один из массивов не закончится, сравнивать текущие элементы обоих массивов.
Если текущий элемент из arr1 меньше или равен текущему элементу из arr2, увеличивать i и добавлять элемент из arr1 к конечному массиву.
В противном случае, увеличивать j и добавлять элемент из arr2 к конечному массиву.
Повторять шаги 2-4 до тех пор, пока не будет достигнута k-я позиция в конечном отсортированном массиве.
 */
public class Search {

        public static int findKthElement(int[] arr1, int[] arr2, int k) {
            int i = 0, j = 0;
            int[] mergedArray = new int[arr1.length + arr2.length];

            while (i < arr1.length && j < arr2.length) {
                if (arr1[i] <= arr2[j]) {
                    mergedArray[i + j] = arr1[i];
                    i++;
                } else {
                    mergedArray[i + j] = arr2[j];
                    j++;
                }
            }

            // Если один из массивов закончился, добавляем оставшиеся элементы из другого массива
            while (i < arr1.length) {
                mergedArray[i + j] = arr1[i];
                i++;
            }

            while (j < arr2.length) {
                mergedArray[i + j] = arr2[j];
                j++;
            }

            // Возвращаем k-й элемент в конечном массиве
            return mergedArray[k - 1];
        }

        public static void main(String[] args) {
            int[] arr1 = {100, 112, 256, 349, 770};
            int[] arr2 = {72, 86, 113, 119, 265, 445, 892};
            int k = 7;

            int result = findKthElement(arr1, arr2, k);
            System.out.println("Output: " + result);
        }
    }


